What are the benefits of using OOP in Ruby?

- Creating objects allows programmers to think more abstractly about the code they are writing
- Objects represented by nouns are easier to conceptualize
- It allows us to only expose functionality to the parts of the code that ned it, meaning namespace
  issues are harder to come across
- It allows us to easily give functionality to different parts of an application without duplication
- We can build applications faster as we can reuse pre-written code
- As software becomes more complex this complexity can be more easily managed.
- Classes and Inheritance help group similar objects together
- Help define a mental model of problem domain
- Help DRY code and reduce redundancy
- Help clean code for easier maintenance and easier to read